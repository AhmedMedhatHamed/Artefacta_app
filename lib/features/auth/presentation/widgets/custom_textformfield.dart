import 'package:artefacta_app/core/utils/app_color/app_color.dart';
import 'package:artefacta_app/core/utils/text_styles/text_styles.dart';
import 'package:flutter/material.dart';

class CustomTextFormField extends StatelessWidget {
  const CustomTextFormField({
    super.key,
    this.controller,
    this.textInputAction,
    this.keyboardType,
    this.validator,
    this.labelText,
    this.prefixIcon,
    this.suffixIcon,
    this.obscureText,
  });
  final TextEditingController? controller;
  final TextInputAction? textInputAction;
  final TextInputType? keyboardType;
  final String? Function(String?)? validator;
  final String? labelText;
  final Widget? prefixIcon;
  final Widget? suffixIcon;
  final bool? obscureText;
  @override
  Widget build(BuildContext context) {
    return TextFormField(
      controller: controller,
      style: CustomTextStyles.poppins300styles16,
      cursorColor: AppColor.primaryColor,
      textInputAction: textInputAction ?? TextInputAction.next,
      keyboardType: keyboardType,
      cursorHeight: 14,
      validator: validator,
      obscureText: obscureText ?? false,
      decoration: InputDecoration(
        filled: true,
        prefixIcon: prefixIcon,
        prefixIconColor: AppColor.primaryColor,
        suffixIcon: suffixIcon,
        suffixIconColor: AppColor.primaryColor,
        labelText: labelText,
        labelStyle: CustomTextStyles.poppins300styles16.copyWith(
          color: Colors.grey,
        ),
        border: OutlineInputBorder(
          borderRadius: BorderRadius.all(Radius.circular(8.0)),
          borderSide: BorderSide(color: Colors.grey),
        ),
        enabledBorder: OutlineInputBorder(
          borderRadius: BorderRadius.all(Radius.circular(8.0)),
          borderSide: BorderSide(color: Colors.grey),
        ),
        focusedBorder: OutlineInputBorder(
          borderRadius: BorderRadius.all(Radius.circular(8.0)),
          borderSide: BorderSide(color: Colors.grey),
        ),
      ),
    );
  }
}
